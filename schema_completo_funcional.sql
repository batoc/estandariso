-- Calcular cumplimiento de compromisos de una revisión
CREATE OR REPLACE FUNCTION calcular_cumplimiento_revision(revision_id_param BIGINT)
RETURNS NUMERIC AS $$
DECLARE
    total_compromisos INT;
    compromisos_completados INT;
    porcentaje NUMERIC;
BEGIN
    SELECT COUNT(*) INTO total_compromisos 
    FROM compromisos 
    WHERE revision_id = revision_id_param;
    
    IF total_compromisos = 0 THEN
        RETURN 100;
    END IF;
    
    SELECT COUNT(*) INTO compromisos_completados 
    FROM compromisos 
    WHERE revision_id = revision_id_param AND estado = 'completado';
    
    porcentaje := (compromisos_completados::NUMERIC / total_compromisos::NUMERIC) * 100;
    
    RETURN ROUND(porcentaje, 2);
END;
$$ LANGUAGE plpgsql;

-- Actualizar compromisos vencidos
CREATE OR REPLACE FUNCTION actualizar_compromisos_vencidos()
RETURNS void AS $$
BEGIN
    UPDATE compromisos
    SET estado = 'vencido'
    WHERE fecha_limite < CURRENT_DATE
    AND estado IN ('pendiente', 'en_proceso');
END;
$$ LANGUAGE plpgsql;

-- Calcular porcentaje de conformidad general
CREATE OR REPLACE FUNCTION calcular_conformidad_general()
RETURNS NUMERIC AS $$
DECLARE
    total_items INT;
    items_conformes INT;
    porcentaje NUMERIC;
BEGIN
    SELECT 
        SUM(items_inspeccionados),
        SUM(items_conformes)
    INTO total_items, items_conformes
    FROM inspecciones_verificacion
    WHERE fecha_inspeccion >= CURRENT_DATE - INTERVAL '3 months';
    
    IF total_items = 0 THEN
        RETURN 100;
    END IF;
    
    porcentaje := (items_conformes::NUMERIC / total_items::NUMERIC) * 100;
    
    RETURN ROUND(porcentaje, 2);
END;
$$ LANGUAGE plpgsql;

